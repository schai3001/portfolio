#include "libft.h"

/*
	DESCRIPTION :
	The function ft_lstadd_front adds a new node to the front of a list:
		[NEW]->[.]->[.]->[.]->[NULL]

	RETURN VALUE :
	None.
*/

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (!new)
		return ;
	if (!*alst)
	{
		*alst = new;
		return ;
	}
	new->next = *alst;
	*alst = new;
}
