#include "libft.h"

/*
	DESCRIPTION :
	The function ft_putendl_fd writes the given string to the given
	file descriptor followed by a new line.

	RETURN VALUE :
	None.
*/

void	ft_putendl_fd(char *s, int fd)
{
	char	*tmp;

	tmp = ft_strjoin(s, "\n");
	ft_putstr_fd(tmp, fd);
	free(tmp);
}
