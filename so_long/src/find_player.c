#include "so_long.h"

t_ull	find_player_x(t_game *game)
{
	t_ull	i;
	t_ull	j;

	i = 0;
	while (game->map[i])
	{
		j = 0;
		while (game->map[i][j])
		{
			if (game->map[i][j] == 'P')
			{
				return (i);
			}
			j++;
		}
		i++;
	}
	return (0);
}

t_ull	find_player_y(t_game *game)
{
	t_ull	i;
	t_ull	j;

	i = 0;
	while (game->map[i])
	{
		j = 0;
		while (game->map[i][j])
		{
			if (game->map[i][j] == 'P')
			{
				return (j);
			}
			j++;
		}
		i++;
	}
	return (0);
}
